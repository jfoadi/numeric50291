import numpy as np # type: ignore

def add(a, b): # Addition function
    return a + b

def subtract(a, b): # Subtraction function
    return a - b

def multiply(a, b): # Multiplication function
    return a * b

def divide(a, b): # Division function
    return a / b

def powerof(a, b):  # Power function
    return a ** b